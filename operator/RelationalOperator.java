package operator;

public class RelationalOperator {
	public static void main(String[] args) {
		int a=5;
		int b=6;
		
		//the return type of relational operator is boolean
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
	}
}
