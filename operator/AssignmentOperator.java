package operator;

public class AssignmentOperator {

	public static void main(String[] args) {
		/*
		  Assignment operator is used to assign values 
		 */
		int a=5;
		// = is used to assign values 
		
		//We can use it to increment values as well
		
		a+=1;
		a-=1;
		//shorthand operators
		
		System.out.println(a);
		
	}

}
