package basics;

public class _4ConditionalOperators {

	public static void main(String[] args) {
		// Conditional operators return boolean values i.e. true or false;
		
		int a =5;
		
		int b=3;
		
		System.out.println(a==b);
		
		System.out.println(a>=b);
		
		System.out.println(a<=b);
		
		System.out.println(a!=b);
		
		System.out.println(a>b);
		
		System.out.println(a<b);
	}

}
