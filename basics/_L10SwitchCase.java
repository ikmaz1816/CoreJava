package basics;

public class _L10SwitchCase {

	public static void main(String[] args) {
		int a=5;
		switch(a)
		{
		case 0:
			System.out.println("1");
			break;
		case 1:
			System.out.println("2");
			break;
		case 2:
			System.out.println("3");
			break;
		default:
			System.out.println("Wrong input");
		}
	}

}
