package basics;

public class Variables {
	public static void main(String[] args) {
		/*
		  We can have different types of variables 
		  As variables are containers to hold different values
		 */
		int a=5;
		//By default in Java decimal values are double
		float f=4f;
		double d=4;
		long l=4L;
		char ch='a';
		boolean flag=true;
		System.out.println(a+" "+f+" "+d+" "+l+" "+ch+" "+flag);
	} 
}
