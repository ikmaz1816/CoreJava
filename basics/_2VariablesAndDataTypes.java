package basics;

public class _2VariablesAndDataTypes {
	public static void main(String[] args)
	{
		//int-> 4 bytes
		// 1 byte=8bit
		//float->4 bytes
		//byte ->1 byte
		//char ->1 byte
		//long and double->8 byte
		int a=5;
		float b= 5.6F;
		//In java all decimal values are considered to be decimal
		//Hence we need to mention it is of floating type by mentioning F at the end of declaration
		double d=8.0;
		long l=560000000;
		char c='d';
		System.out.println(a+" "+b+" "+d+" "+" "+l+" "+c);
	}
}
