package inheritance;

public final class FinalClass {
	/*
	 	So when we write final keyword before class, class cannot be extended
	 	When we make a variable final values cannot be changed
	 	When we make a call method final it cannot be overriden
	 */
	public static final int a=5;
	
	public final void display()
	{
		System.out.println("In display");
	}
	
	
	
}
